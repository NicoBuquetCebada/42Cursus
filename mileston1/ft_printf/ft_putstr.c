#include "ft_printf.h"

void	ft_putstr(char *s, int *cs)
{
	unsigned int	i;

	if (!s)
	{
		write(1, "(null)", 6);
		(*cs) += 6;
		return ;
	}
	i = -1;
	while (s[++i])
	{
		write(1, &s[i], 1);
		(*cs)++;
	}
}
