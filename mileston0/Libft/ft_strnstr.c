#include "libft.h"

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	size_t			i;

	i = 0;
	if (little[i] == '\0')
		return ((char *)big);
	while (big[i] && i < len && ft_strlen(little) <= len - i)
	{
		if (ft_strncmp(&big[i], little, ft_strlen(little)) == 0)
			return ((char *)&big[i]);
		i++;
	}
	return (NULL);
}
