#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*t1;
	t_list	*t2;

	t1 = *lst;
	while (t1)
	{
		del (t1 -> content);
		t2 = t1 -> next;
		free(t1);
		t1 = t2;
	}
	*lst = NULL;
}
